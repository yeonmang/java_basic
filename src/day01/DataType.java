package day01;

public class DataType {
    public static void main(String[] args) {
        // 정수 타입
        byte a = 127;
        short b = 32767;
        int c = 2147483647;
        long d = 2147483647;

        float f1 = 1.2345678901234F;
        double d1 = 1.234567891234;

        System.out.println("f1 = " + f1);
        System.out.println("d1 = " + d1);

    }




}
